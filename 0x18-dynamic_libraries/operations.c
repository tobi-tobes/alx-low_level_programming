#include <stdio.h>

/**
 * add - adds two variables
 * @a: variable a
 * @b: variable b
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two variables
 * @a: variable a
 * @b: variable b
 *
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two variables
 * @a: variable a
 * @b: variable b
 *
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two variables
 * @a: variable a
 * @b: variable b
 *
 * Return: quotient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns remainder of division of two variables
 * @a: variable a
 * @b: variable b
 *
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
